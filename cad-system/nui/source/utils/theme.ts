export type ThemeName = keyof typeof themes;

let currentTheme: ThemeName = 'dos';

export function applyTheme(themeName: ThemeName): void {
  const theme = themes[themeName];
  if (!theme) {
    console.warn(`Theme "${themeName}" not found`);
    return;
  }

  currentTheme = themeName;

  if (typeof localStorage !== 'undefined') {
    localStorage.setItem('tui-theme', themeName);
  }

  const body = document.body;
  if (body) {
    body.className = body.className.replace(/\b(blue|purple|green|cyan|red|yellow|orange|black|white)-(?:168|255)(?:-text|-border)?\b/g, '');
    body.className = body.className.replace(/\btui-scroll-(blue|green|cyan|red|purple|yellow|white|orange)\b/g, '');
    body.classList.add(...theme.classes.body.split(' '));
    if (theme.classes.scrollbar) {
      body.classList.add(theme.classes.scrollbar);
    }
  }

  if (typeof window !== 'undefined') {
    window.dispatchEvent(new CustomEvent('themechange', { detail: themeName }));
  }
}

export function getCurrentTheme(): ThemeName {
  return currentTheme;
}

export function getThemeClasses(themeName: ThemeName): typeof themes[ThemeName]['classes'] {
  return themes[themeName]?.classes || themes.dos.classes;
}

export function initTheme(): void {
  if (typeof localStorage !== 'undefined') {
    const saved = localStorage.getItem('tui-theme') as ThemeName;
    if (saved && themes[saved]) {
      applyTheme(saved);
    } else {
      applyTheme('dos');
    }
  } else {
    applyTheme('dos');
  }
}

export const availableThemes = Object.entries(themes).map(([key, theme]) => ({
  key: key as ThemeName,
  name: theme.name
}));
